class ClientModel {
  constructor(cid, id, firstName, lastName, address, city, phoneNumber, email) {
    this.cid = cid;
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.address = address;
    this.city = city;
    this.phoneNumber = phoneNumber;
    this.email = email;
  }
}

export default ClientModel;
